package book;

abstract public class Books {

    protected String id;
    protected String title;
    protected String author;
    protected int year;

    public Books(String id, String title, String author, int year) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.year = year;
    }

    public String getBookId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }

    public int getYear() {
        return year;
    }

    
    
    
}
